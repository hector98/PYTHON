#Strings como Listas
my_string = "Aprendiendo Python3"

print ("El string es: "+my_string)
#mostrar un caracter en especifico
print("Mostrar caracter que esta en la pocision 14: "+my_string[14])

#mostrar el primero y ultimo caracter
print("Mostrar el primer y ultimo caracter: "+my_string[0], my_string[-1])

#Mostrar una cadena de caracteres en especifico
print("Mostrar una cadena de caracteres de la pocision 0 a la 15: "+my_string[0:15])

#Motrar cadena de caracteres con saltos por ejemplo de dos en dos
print("Mostrar una cadena de caracteres de la pocision 0 a la 15 de dos en dos: "+my_string[0:15:2])

#Mostrar la cadena al reves
print("Mostrar el String al reves: "+my_string[::-1])
